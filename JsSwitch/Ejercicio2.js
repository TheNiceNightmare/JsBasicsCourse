//// switch & "break"

var luckyNumber = 1;

switch (luckyNumber) {
    case 1:
        console.log('Is 1');  //"Is 1"
    case 8:
        console.log('Is 8');   //"Is 8"
    default:
        console.log('Default')   //"Default"
}

